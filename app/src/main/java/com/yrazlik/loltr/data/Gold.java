package com.yrazlik.loltr.data;

public class Gold {
	
	private int total, sell, base;
	private boolean purchsable;
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getSell() {
		return sell;
	}
	public void setSell(int sell) {
		this.sell = sell;
	}
	public int getBase() {
		return base;
	}
	public void setBase(int base) {
		this.base = base;
	}
	public boolean isPurchsable() {
		return purchsable;
	}
	public void setPurchsable(boolean purchsable) {
		this.purchsable = purchsable;
	}
	
	

}
